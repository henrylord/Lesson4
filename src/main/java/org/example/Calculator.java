package org.example;

public class Calculator {

    public double addition( double x, double y){

        return x+y;

    }

    public double substraction(double x, double y){

        return x-y;
    }

    public double multiplication(double x, double y){

        return x*y;
    }

    public double division(double x, double y){

        return x/y;
    }

}
